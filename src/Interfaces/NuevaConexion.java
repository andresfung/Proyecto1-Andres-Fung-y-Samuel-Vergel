    private void agregarConexionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarConexionActionPerformed
        String nombreO = (String) proteinaOrigen.getSelectedItem();
        String nombreD = (String) proteinaDestino.getSelectedItem();
        Validacion validar = new Validacion();
        if(validar.ValidarNumeros(pesoInput.getText()) != -1){
            int peso = validar.ValidarNumeros(pesoInput.getText());
            appGrafo.addArist(nombreD, nombreO, peso);
            JOptionPane.showMessageDialog(null, "Conexion agregada correctamente.");
        }else{
            JOptionPane.showMessageDialog(null, "El peso debe ser un numero entero psoitivo.");
        }
    }//GEN-LAST:event_agregarConexionActionPerformed

