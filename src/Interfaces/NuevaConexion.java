    private void actualizarModeloCombo() {
        modeloCombo.removeAllElements();
        modeloCombo2.removeAllElements();

        if (!appGrafo.isEmpty()) {
            for (int i = 0; i < appGrafo.getVerts().getSize(); i++) {
                Vertice verticeActual = (Vertice) appGrafo.getVerts().getValue(i);
                modeloCombo.addElement(verticeActual.getNombre());
            }

            if (modeloCombo.getSize() > 0) {
                proteinaOrigen.setSelectedIndex(0);
                actualizarComboDestino();
            }
        }
    }

    private void actualizarComboDestino() {
        modeloCombo2.removeAllElements();

        if (!appGrafo.isEmpty()) {
            String proteinaO = (String) proteinaOrigen.getSelectedItem();

            for (int i = 0; i < appGrafo.getVerts().getSize(); i++) {
                Vertice verticeActual = (Vertice) appGrafo.getVerts().getValue(i);
                String nombre = verticeActual.getNombre();

                //No agregamos la proteina principal en el combo de "destinoS"
                if (!nombre.equals(proteinaO)) {
                    modeloCombo2.addElement(nombre);
                }
            }

            if (modeloCombo2.getSize() > 0) {
                proteinaDestino.setSelectedIndex(0);
            }
        }
    }

